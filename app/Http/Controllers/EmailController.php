    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        $emailVal = $request->input('email');
        $email = new Email;

        if (Email::where('email', '=', $request->input('email'))->exists()) {
            return redirect('/join/create')->with('error', 'Email In Use');
         }

        if(filter_var($emailVal, FILTER_VALIDATE_EMAIL)) {
            $email->email = $emailVal;
            $email->save();
            return redirect('/')->with('success', 'Thanks For Joining!');
        } else {
            return redirect('/join/create')->with('error', 'Invalid Email');
        }

    }

